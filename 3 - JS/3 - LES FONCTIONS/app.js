// 1 - déclarer la fonction
function calculerLeCarre(nombre) {
    return nombre * nombre;
}

// 2 - utiliser la fonction
// mettre simplement un nombre dans les paranthèses
calculerLeCarre(34)


// on peut sauvegarder le résultat de ce calcul dans une variable et l'affficher en console.
